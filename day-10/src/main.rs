use std::collections::HashSet;

fn gcd(a: i32, b: i32) -> i32
{
	let (mut a, mut b) = if a > b {
		(a, b)
	} else {
		(b, a)
	};

	while b != 0 {
		let r = a % b;
		a = b;
		b = r;
	}

	a.abs()
}

fn normalise(x: i32, y: i32) -> (i32, i32)
{
	let gcd = gcd(x, y);
	(x / gcd, y / gcd)
}

fn count_points(ax: i32, ay: i32, input: [[i32; 34]; 34]) -> usize
{
	let mut points = HashSet::<(i32, i32)>::new();
	for (y, inner) in input.iter().enumerate()
	{
		let by = y as i32;
		for (x, asteroid) in inner.iter().enumerate()
		{
			let bx = x as i32;
			if *asteroid == 1
			{
				if ax == bx && ay == by
				{
				}
				else if ax == bx
				{
					points.insert((0, if ay < by { 1 } else { -1 }));
				}
				else if ay == by
				{
					points.insert((if ax < bx { 1 } else { -1 }, 0));
				}
				else
				{
					let pos = normalise(bx - ax, by - ay);
					points.insert(pos);
				}
			}
			//let count = count_points();
		}
	}
	points.len()
}

